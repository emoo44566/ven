# تعریف دو متغیر
variable1 = 10
variable2 = 20

# نمایش مقادیر اولیه
print("متغیر 1 قبل از جابجایی:", variable1)
print("متغیر 2 قبل از جابجایی:", variable2)

# جابجایی مقادیر بین دو متغیر
temp_variable = variable1
variable1 = variable2
variable2 = temp_variable

# نمایش مقادیر پس از جابجایی
print("متغیر 1 پس از جابجایی:", variable1)
print("متغیر 2 پس از جابجایی:", variable2)